internal class OrchardCodePackagesSetup
{
    private SourceRepository source;
    private string gpf;
    private SourceCacheContext cacheContext;

    public OrchardCodePackagesSetup()
    {
        source = Repository.Factory.GetCoreV3("https://api.nuget.org/v3/index.json");
        var settings = NuGet.Configuration.Settings.LoadDefaultSettings(Environment.CurrentDirectory);
        gpf = NuGet.Configuration.SettingsUtility.GetGlobalPackagesFolder(settings);
        cacheContext = new SourceCacheContext();
    }

    public async Task<IReadOnlyList<string>> GetPackages()
    {
        var packageIdentities = GetPackageIdentities();
        var packages = new string[packageIdentities.Count];
        int index = 0;
        var tasks = new Task[Math.Min(4, packageIdentities.Count)];

        for (int i = 0; i < tasks.Length; i++)
        {
            var packageIndex = index++;
            tasks[i] = Task.Run(() => GetPackagePath(packageIdentities[packageIndex], packages, packageIndex));
        }

        while (index < packageIdentities.Count)
        {
            await Task.WhenAny(tasks);
            for (int i = 0; i < tasks.Length; i++)
            {
                if (tasks[i].IsCompleted)
                {
                    var packageIndex = index++;
                    tasks[i] = Task.Run(() => GetPackagePath(packageIdentities[packageIndex], packages, packageIndex));
                    break;
                }
            }
        }

        await Task.WhenAll(tasks);

        return Array.AsReadOnly(packages);
    }

    private async Task GetPackagePath(PackageIdentity packageIdentity, string[] packages, int destinationIndex)
    {
        try
        {
            var packageId = packageIdentity.Id.ToLowerInvariant();
            var packageVersion = packageIdentity.Version.ToNormalizedString();
            var nupkgName = packageId + "." + packageVersion + ".nupkg";
            var gpfPath = Path.Combine(gpf, packageId, packageVersion, nupkgName);
            if (File.Exists(gpfPath))
            {
                packages[destinationIndex] = gpfPath;
            }
            else
            {
                var nupkgDirectory = GetNupkgDirectory();
                if (!Directory.Exists(nupkgDirectory)) Directory.CreateDirectory(nupkgDirectory);
                var nupkgPath = Path.Combine(nupkgDirectory, nupkgName);
                if (File.Exists(nupkgPath))
                {
                    packages[destinationIndex] = nupkgPath;
                }
                else
                {
                    var resource = await source.GetResourceAsync<FindPackageByIdResource>();
                    var downloader = await resource.GetPackageDownloaderAsync(packageIdentity, cacheContext, NullLogger.Instance, CancellationToken.None);
                    await downloader.CopyNupkgFileToAsync(nupkgPath, CancellationToken.None);
                    packages[destinationIndex] = nupkgPath;
                }
            }
        }
        catch (Exception ex)
        {
            // for debugging, breakpoint and local variable inspection.
            _ = ex;
            throw;
        }
    }

    private string GetNupkgDirectory()
    {
        // BenchmarkDotNet runs with the CWD as the temporary generated project's bin folder.
        // In order to ensure that the nupkgs don't need to get re-downloaded, even when
        // debugging this app (by editing Program.cs and calling a benchmark class directly)
        // use the project's obj folder as the parent directory for nupkgs.
        var projectDirectory = Environment.CurrentDirectory;
        while (!File.Exists(Path.Combine(projectDirectory, "WritePerformance.csproj")))
        {
            var newDirectory = Path.GetDirectoryName(projectDirectory);
            if (string.IsNullOrEmpty(newDirectory) || newDirectory == projectDirectory)
            {
                throw new FileNotFoundException("Could not find WritePerformance.csproj");
            }
            projectDirectory = newDirectory;
        }

        var nupkgDirectory = Path.Combine(projectDirectory, "obj", "nupkgs");
        return nupkgDirectory;
    }

